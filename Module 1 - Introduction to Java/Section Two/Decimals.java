public class Decimals {

    public static void main(String[] args) {

        double percentage = 55.70;
        double age = 26;

        System.out.println("In 2020, " + percentage + " % was urban.");
        System.out.println("This single digit, " + age + ", is stored as a double.");

        double dividend = 25;
        double divisor = 2;

        System.out.println(dividend / divisor);
    }
}
